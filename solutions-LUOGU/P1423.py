s = float(input())  # 要游的目标距离
v = 2  # 小玉第一步能游的米数
step = 0  # 步数
currS = 0  # 小玉当前步游的米数
while currS < s:
    step += 1
    currS += v * 1
    v *= 0.98
print(step)
