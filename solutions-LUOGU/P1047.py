# hint
# 本题有两种解法，(1)模拟的方法；(2)线段树
# 模拟的解法：首先定义一个数组用来记录树木的数量，用两
# 层循环模拟构架地铁，外层循环遍历每一个区域，内层循环
# 模拟区域内需要砍掉的树木（在区域内的树木，数组在该点
# 的索引值为0）
################################################
l, m = map(int, input().split(' '))
tree = [1] * (l + 1)  # 创建一个数组用来记录树木的数量

for i in range(m):
    u, v = map(int, input().split(' '))
    for j in range(u, v + 1):
        if tree[j] != 0:
            tree[j] = 0
print(sum(tree))
