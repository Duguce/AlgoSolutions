# 本题用到了栈的思想，后进先出
################################################
a = list(map(int, input().split(' ')))
b = []
for i in a:
    if i != 0:
        b.insert(0, i)  # 后进先出原则，把新的元素插入在前面
    else:
        break
print(*b, sep=' ')
